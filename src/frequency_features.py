def extract_frequency_features_from_array(gray, n_fft_bins=100, dct_block_size=8):
    """
    Same as extract_frequency_features but accepts a pre-loaded grayscale
    float32 numpy array of shape (H, W) with values in [0, 255].

    Used by step5_gradio_demo where the image arrives as a PIL object
    rather than a file path.
    """
    fft_features = extract_fft_features(gray, n_bins=n_fft_bins)
    dct_features = extract_dct_features(gray, block_size=dct_block_size)
    return np.concatenate([fft_features, dct_features]).astype(np.float32)


def extract_frequency_features(image_path, n_fft_bins=100, dct_block_size=8):
    """
    Extract a combined frequency-domain feature vector from a single image.

    This is the only function step2_extract_features.py calls.

    Combines:
      FFT path  (Dzanic et al., NeurIPS 2020):
        - 100-bin azimuthally-averaged log radial power spectrum
        -   2 power-law decay parameters (b1, b2)
        = 102 dimensions

      DCT path  (Frank et al., ICML 2020):
        - 63 AC coefficient means  across 8x8 blocks
        - 63 AC coefficient stds   across 8x8 blocks
        = 126 dimensions

      Total: 228 dimensions.

    CONTENT INVARIANCE
    A face photo and a landscape photo generated by the same GAN will have
    similar frequency feature vectors, because the upsampling artifacts that
    both feature paths detect are imposed by the generator architecture, not
    by the image content. Real photos from either scene will share the natural
    power-law decay and natural DCT statistics. This is the key property that
    makes frequency features complementary to CNN features.

    Args:
        image_path:     path to a JPEG/PNG image file.
                        The image will be internally resized to 299×299 to
                        match the images produced by step1.
        n_fft_bins:     number of radial bins for the FFT spectrum (default 100).
        dct_block_size: tile size for block DCT (default 8, JPEG-style).

    Returns:
        features: float32 numpy array of shape (228,) with default arguments.
                  Indices   0 – 99:  FFT radial spectrum (log-compressed)
                  Indices 100 – 101: FFT decay params b1, b2
                  Indices 102 – 164: DCT AC coefficient means
                  Indices 165 – 227: DCT AC coefficient stds
    """
    np.random.seed(42)   # ensures reproducibility if any stochastic step is added later

    # --- Step 1: Load and convert to grayscale ---
    # Both paths operate on luminance; computing grayscale once and reusing
    # it avoids loading the image twice.
    gray = _load_grayscale(image_path)

    # --- Step 2: FFT path (Dzanic et al.) ---
    # 100-bin radial power spectrum + 2 decay parameters = 102-d
    fft_features = extract_fft_features(gray, n_bins=n_fft_bins)   # (102,)

    # --- Step 3: DCT path (Frank et al.) ---
    # Mean + std of 63 AC DCT coefficients = 126-d
    dct_features = extract_dct_features(gray, block_size=dct_block_size)   # (126,)

    # --- Step 4: Concatenate ---
    combined = np.concatenate([fft_features, dct_features])   # (228,)

    return combined.astype(np.float32)
